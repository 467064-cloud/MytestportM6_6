print("=== โปรแกรมคำนวณพื้นที่อเนกประสงค์ ===")

while True:
    print("\n--- เมนูหลัก ---")
    print("1. รูปสี่เหลี่ยมมุมฉาก")
    print("2. รูปสามเหลี่ยม")
    print("พิมพ์ 'exit' หรือ 'ออก' เพื่อจบโปรแกรม")
    
    เมนู = input("เลือกเมนูที่ต้องการ (1, 2 หรือ พิมพ์ออก): ").strip()

    # ตรวจสอบว่าผู้ใช้ต้องการออกจากโปรแกรมหรือไม่
    if เมนู.lower() == "exit" or เมนู == "ออก":
        print("ขอบคุณที่ใช้งานโปรแกรม! สวัสดีครับ")
        break # หลุดออกจาก while loop ทันที

    elif เมนู == "1":
        กว้าง = float(input("กรอกความกว้าง (เมตร): "))
        ยาว = float(input("กรอกความยาว (เมตร): "))
        พื้นที่ = กว้าง * ยาว
        print(f"-> พื้นที่สี่เหลี่ยมคือ: {พื้นที่} ตารางเมตร")

    elif เมนู == "2":
        ฐาน = float(input("กรอกความยาวฐาน (เมตร): "))
        สูง = float(input("กรอกความสูง (เมตร): "))
        พื้นที่ = 0.5 * ฐาน * สูง
        print(f"-> พื้นที่สามเหลี่ยมคือ: {พื้นที่} ตารางเมตร")
        
    else:
        print("❌ คุณกรอกเมนูไม่ถูกต้อง กรุณาเลือก 1, 2 หรือพิมพ์ 'ออก'")
